#include <iostream>

using namespace std;

// argc and argv are not required, however they provide a way to access console parameters
int main(int argc, char const *argv[]) {
    /*
    'cout' is console out
    '<< endl' is optional, and adds a new line to the output
    */
    cout << "Hello!" << endl;

    // 'return' is optional in cpp
    return 0;
}